import { ref } from "vue";
import { useQuasar } from "quasar";

export default defineComponent({
  name: "IndexPage",
  components: {
    ProgressStepper,
    QuestionComponent,
    FlowStep1,
    FlowStep2,
    FlowStep3,
    FlowStep4,
    FlowStep5,
  },
  setup () {
    const $q = useQuasar();
    const questionActive = ref(0);
    const assetPrice = ref(0);
    const step = ref(1);

    const nextStep = () => {
      window.scrollTo(0, 0);
      step.value += 1;
    };

    const toggleOpenedQuestion = (index) => {
      if (questionActive.value === index) {
        questionActive.value = 0;
        return;
      }
      questionActive.value = index;
    };

    const stepClicked = (index) => {
      if (step.value < index) {
        return
      }
      if (step.value !== index) {
        window.scrollTo(0, 0);
        step.value = index;
      }
    };

    return {
      step,
      questionActive,
      toggleOpenedQuestion,
      assetPrice,
      nextStep,
      stepClicked,
      isMobile: ref($q.platform.is.mobile),
    };
  },
});
